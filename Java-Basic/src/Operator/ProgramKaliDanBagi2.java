public class ProgramKaliDanBagi2 {
    public static void main(String[] args) {
        int angka = 10;

        int hasilPerkalian = angka << 1;

        int hasilPembagian = angka >> 1;

        System.out.println("Hasil perkalian " + angka + " dengan 2: " + hasilPerkalian);
        System.out.println("Hasil pembagian " + angka + " dengan 2: " + hasilPembagian);
    }
}
